// Exemplo de função simples em JavaScript
function soma(a, b) {
    return a + b;
}

console.log(soma(5, 3));

// Exemplo de função com parâmetros padrão
const raiz = function (x) {
    return x ** 0.5;
}

console.log(raiz(9));

// Exemplo de uma aerow function
const raiz2 = n => n ** 0.5;

console.log(raiz2(16));